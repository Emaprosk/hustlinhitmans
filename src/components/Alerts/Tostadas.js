import { toast } from "react-toastify";

export const toastSuccess = (mesagge) => {
    toast.success(mesagge, {
        position: "bottom-center",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "colored",
    });
};

